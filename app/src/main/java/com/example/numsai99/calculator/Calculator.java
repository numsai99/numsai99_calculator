public class Calculator {
    public double addPlus(double one,double two) {
        return one + two;
    }

    public double addMinus(double one, double two) {
        return one - two;
    }

    public double addMulti(double one, double two) {
        return one * two;
    }

    public double addDiv(double one, double two) {
        return one / two;
    }

}
